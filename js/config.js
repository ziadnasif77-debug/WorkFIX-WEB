function formatCurrency(amount, currency) {
  try {
    return new Intl.NumberFormat(state.lang === 'ar' ? 'ar-SA' : state.lang === 'no' ? 'nb-NO' : state.lang === 'sv' ? 'sv-SE' : 'en-US', {
      style: 'currency', currency: currency || 'SAR', minimumFractionDigits: 0, maximumFractionDigits: 2
    }).format(amount || 0);
  } catch { return `${amount} ${currency}`; }
}

function formatDate(dateStr) {
  if (!dateStr) return '';
  const d = new Date(dateStr);
  const loc = state.lang === 'ar' ? 'ar-SA' : state.lang === 'no' ? 'nb-NO' : state.lang === 'sv' ? 'sv-SE' : 'en-US';
  return d.toLocaleDateString(loc, { month: 'short', day: 'numeric', year: 'numeric' });
}

function formatTime(dateStr) {
  if (!dateStr) return '';
  const d = new Date(dateStr);
  return d.toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' });
}

function timeAgo(dateStr) {
  if (!dateStr) return '';
  const diff = Date.now() - new Date(dateStr).getTime();
  const mins = Math.floor(diff / 60000);
  if (mins < 1) return t('today');
  if (mins < 60) return `${mins}m ${t('ago')}`;
  const hrs = Math.floor(mins / 60);
  if (hrs < 24) return `${hrs}h ${t('ago')}`;
  const days = Math.floor(hrs / 24);
  if (days === 1) return t('yesterday');
  return `${days}d ${t('ago')}`;
}

